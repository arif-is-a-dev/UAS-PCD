def make_road_roi_mask(frame):
    height, width = frame.shape[:2]
    mask = np.zeros((height, width), dtype=np.uint8)

    road_polygon = np.array([
        (0, int(height * 0.40)),
        (int(width * 0.42), int(height * 0.23)),
        (int(width * 0.64), int(height * 0.23)),
        (width, int(height * 0.33)),
        (width, height),
        (0, height),
    ], dtype=np.int32)

    cv2.fillPoly(mask, [road_polygon], 255)
    return mask


def apply_roi(mask, roi_mask):
    return cv2.bitwise_and(mask, roi_mask)

"""## 4. Fungsi deteksi konservatif"""

def preprocess_frame(frame, width=960):
    h, w = frame.shape[:2]
    scale = width / w
    resized = cv2.resize(frame, (width, int(h * scale)))
    gray = cv2.cvtColor(resized, cv2.COLOR_BGR2GRAY)
    gray = cv2.GaussianBlur(gray, (5, 5), 0)
    return resized, gray


def clean_mask_conservative(mask):
    # Open: hapus noise titik kecil
    kernel_open = cv2.getStructuringElement(cv2.MORPH_ELLIPSE, (3, 3))
    # Close: tutup celah kecil dalam blob kendaraan
    kernel_close = cv2.getStructuringElement(cv2.MORPH_RECT, (7, 7))
    # Dilate: perlebar sedikit, DILATE_ITER=1 agar blob tidak bergabung
    kernel_dilate = cv2.getStructuringElement(cv2.MORPH_RECT, (5, 3))

    mask = cv2.morphologyEx(mask, cv2.MORPH_OPEN,  kernel_open,  iterations=2)
    mask = cv2.morphologyEx(mask, cv2.MORPH_CLOSE, kernel_close, iterations=1)
    if DILATE_ITER > 0:
        mask = cv2.dilate(mask, kernel_dilate, iterations=DILATE_ITER)
    return mask


def create_mog2_mask(background_subtractor, current_frame):
    mask = background_subtractor.apply(current_frame)
    _, mask = cv2.threshold(mask, 220, 255, cv2.THRESH_BINARY)
    return clean_mask_conservative(mask)


def create_running_avg_mask(avg_background, current_gray):
    cv2.accumulateWeighted(current_gray, avg_background, RUNNING_AVG_ALPHA)
    background_model = cv2.convertScaleAbs(avg_background)
    frame_delta = cv2.absdiff(current_gray, background_model)
    _, mask = cv2.threshold(frame_delta, RUNNING_AVG_THRESHOLD, 255, cv2.THRESH_BINARY)
    return clean_mask_conservative(mask), avg_background


def min_area_by_perspective(center_y, frame_height):
    y_ratio = center_y / frame_height
    if y_ratio < 0.42:
        return MIN_AREA_TOP
    if y_ratio < 0.68:
        return MIN_AREA_MIDDLE
    return MIN_AREA_BOTTOM


def max_box_size_by_perspective(center_y, frame_width, frame_height):
    y_ratio = center_y / frame_height
    if y_ratio < 0.42:
        max_w = frame_width  * 0.09
        max_h = frame_height * 0.10
    elif y_ratio < 0.68:
        max_w = frame_width  * 0.18
        max_h = frame_height * 0.20
    else:
        max_w = frame_width  * 0.30
        max_h = frame_height * 0.36
    return max_w, max_h


def box_inside_reasonable_perspective(x, y, w, h, frame_width, frame_height):
    center_y = y + h / 2
    max_w, max_h = max_box_size_by_perspective(center_y, frame_width, frame_height)
    if w > max_w or h > max_h:
        return False
    if w * h > frame_width * frame_height * MAX_BOX_AREA_RATIO:
        return False
    return True


def find_vehicle_candidates(mask):
    frame_height, frame_width = mask.shape[:2]
    contours, _ = cv2.findContours(mask, cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_SIMPLE)
    boxes = []

    for contour in contours:
        area = cv2.contourArea(contour)
        x, y, w, h = cv2.boundingRect(contour)
        if w < 10 or h < 10:
            continue

        center_y = y + h / 2
        min_area = min_area_by_perspective(center_y, frame_height)
        if area < min_area:
            continue
        aspect_ratio = w / h
        if aspect_ratio < 0.50 or aspect_ratio > 4.0:
            continue
        bbox_area = w * h
        rectangularity = area / bbox_area if bbox_area > 0 else 0
        if rectangularity < 0.25:
            continue

        if not box_inside_reasonable_perspective(x, y, w, h, frame_width, frame_height):
            continue

        boxes.append([x, y, x + w, y + h, float(area)])

    return boxes


def iou(box_a, box_b):
    ax1, ay1, ax2, ay2, _ = box_a
    bx1, by1, bx2, by2, _ = box_b
    ix1 = max(ax1, bx1); iy1 = max(ay1, by1)
    ix2 = min(ax2, bx2); iy2 = min(ay2, by2)
    iw = max(0, ix2 - ix1); ih = max(0, iy2 - iy1)
    inter = iw * ih
    area_a = max(0, ax2 - ax1) * max(0, ay2 - ay1)
    area_b = max(0, bx2 - bx1) * max(0, by2 - by1)
    union  = area_a + area_b - inter
    return inter / union if union > 0 else 0


def nms(boxes, threshold=0.20):
    selected = []
    boxes = sorted(boxes, key=lambda b: b[4], reverse=True)
    while boxes:
        current = boxes.pop(0)
        selected.append(current)
        boxes = [b for b in boxes if iou(current, b) < threshold]
    return selected


def remove_boxes_inside_larger_boxes(boxes):
    kept = []
    for i, box in enumerate(boxes):
        x1, y1, x2, y2, _ = box
        area = (x2 - x1) * (y2 - y1)
        inside = False
        for j, other in enumerate(boxes):
            if i == j:
                continue
            ox1, oy1, ox2, oy2, _ = other
            other_area = (ox2 - ox1) * (oy2 - oy1)
            if other_area <= area:
                continue
            if x1 >= ox1 and y1 >= oy1 and x2 <= ox2 and y2 <= oy2:
                inside = True
                break
        if not inside:
            kept.append(box)
    return kept


def postprocess_boxes(boxes):
    boxes = nms(boxes, threshold=0.20)
    boxes = remove_boxes_inside_larger_boxes(boxes)
    return boxes

