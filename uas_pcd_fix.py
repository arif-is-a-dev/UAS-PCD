def process_video():
    cap = cv2.VideoCapture(VIDEO_PATH)
    if not cap.isOpened():
        raise RuntimeError(f'Gagal membuka video: {VIDEO_PATH}')

    fps = cap.get(cv2.CAP_PROP_FPS) or 25

    ret, first_frame = cap.read()
    if not ret:
        cap.release()
        raise RuntimeError('Video tidak memiliki frame.')

    first_resized, first_gray = preprocess_frame(first_frame, width=RESIZE_WIDTH)
    height, width = first_resized.shape[:2]

    fourcc = cv2.VideoWriter_fourcc(*'mp4v')
    writer = cv2.VideoWriter(OUTPUT_PATH, fourcc, fps, (width, height))

    background_subtractor = cv2.createBackgroundSubtractorMOG2(
        history=300, varThreshold=32, detectShadows=True
    )
    avg_background = np.float32(first_gray)

    frame_number     = 1
    total_detections = 0
    interval_counts  = []
    review_log       = []
    interval_start   = 1

    print(f'Warmup {WARMUP_FRAMES} frame...')
    for _ in range(WARMUP_FRAMES - 1):
        ret, frame = cap.read()
        if not ret:
            break
        _, wgray = preprocess_frame(frame, width=RESIZE_WIDTH)
        cv2.accumulateWeighted(wgray, avg_background, RUNNING_AVG_ALPHA)
        frame_number += 1
    print('Warmup selesai. Mulai deteksi...')

    while True:
        ret, frame = cap.read()
        if not ret:
            break

        current_frame, current_gray = preprocess_frame(frame, width=RESIZE_WIDTH)
        roi_mask = make_road_roi_mask(current_frame)

        if METHOD == 'running_avg':
            mask, avg_background = create_running_avg_mask(avg_background, current_gray)
        elif METHOD == 'mog2':
            mask = create_mog2_mask(background_subtractor, current_frame)
        else:
            raise ValueError(f'METHOD tidak dikenal: {METHOD}')

        mask  = apply_roi(mask, roi_mask)
        boxes = find_vehicle_candidates(mask)
        boxes = postprocess_boxes(boxes)

        count_this_frame = len(boxes)
        total_detections += count_this_frame
        interval_counts.append(count_this_frame)

        result = draw_detections(current_frame.copy(), boxes)
        writer.write(result)

        if frame_number % REVIEW_INTERVAL == 0:
            avg_iv = sum(interval_counts) / len(interval_counts)
            entry  = (
                f'Frame {interval_start:>5} - {frame_number:>5} | '
                f'Avg: {avg_iv:5.2f} | '
                f'Max: {max(interval_counts):>3} | '
                f'Min: {min(interval_counts):>3} | '
                f'Total: {sum(interval_counts):>5}'
            )
            review_log.append(entry)
            print(entry)
            interval_counts = []
            interval_start  = frame_number + 1

        frame_number += 1

    cap.release()
    writer.release()

    if interval_counts:
        avg_iv = sum(interval_counts) / len(interval_counts)
        entry  = (
            f'Frame {interval_start:>5} - {frame_number - 1:>5} | '
            f'Avg: {avg_iv:5.2f} | '
            f'Max: {max(interval_counts):>3} | '
            f'Min: {min(interval_counts):>3} | '
            f'Total: {sum(interval_counts):>5}  (sisa frame)'
        )
        review_log.append(entry)
        print(entry)

    print()
    print('SELESAI')
    print(f'Output          : {OUTPUT_PATH}')
    print(f'Metode          : {METHOD}')
    print(f'Jumlah frame    : {frame_number - 1}')
    print(f'Total deteksi   : {total_detections}')
    print(f'Rata-rata/frame : {total_detections / max(1, frame_number - 1):.2f}')
    print('=' * 60)
    return review_log

