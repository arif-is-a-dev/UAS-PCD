"""## 8b. Review & Analisis Jumlah Kendaraan per Interval

Cell ini membaca ulang video, menghitung jumlah kendaraan yang terdeteksi setiap `REVIEW_INTERVAL` frame setelah warmup, lalu menampilkan tabel dan grafik ringkasan.

"""

def review_detections_per_interval(
    video_path=VIDEO_PATH,
    interval=REVIEW_INTERVAL,
    warmup_frames=WARMUP_FRAMES,
    save_path='review_deteksi_interval.png'
):
    if interval <= 0:
        raise ValueError('interval harus lebih besar dari 0.')

    cap = cv2.VideoCapture(video_path)
    if not cap.isOpened():
        raise RuntimeError(f'Gagal membuka video: {video_path}')

    fps = cap.get(cv2.CAP_PROP_FPS) or 25

    ret, first_frame = cap.read()
    if not ret:
        cap.release()
        raise RuntimeError('Video tidak memiliki frame.')

    first_resized, first_gray = preprocess_frame(first_frame, width=RESIZE_WIDTH)
    avg_background = np.float32(first_gray)

    background_subtractor = cv2.createBackgroundSubtractorMOG2(
        history=300,
        varThreshold=32,
        detectShadows=True
    )

    last_read_frame_number = 1

    while last_read_frame_number < warmup_frames:
        ret, frame = cap.read()
        if not ret:
            break

        warm_frame, warm_gray = preprocess_frame(frame, width=RESIZE_WIDTH)

        if METHOD == 'running_avg':
            cv2.accumulateWeighted(warm_gray, avg_background, RUNNING_AVG_ALPHA)
        elif METHOD == 'mog2':
            background_subtractor.apply(warm_frame)
        else:
            cap.release()
            raise ValueError(f'METHOD tidak dikenal: {METHOD}')

        last_read_frame_number += 1

    summary = []
    counts_buffer = []
    interval_start = last_read_frame_number + 1

    while True:
        ret, frame = cap.read()
        if not ret:
            break

        current_frame_number = last_read_frame_number + 1
        current_frame, current_gray = preprocess_frame(frame, width=RESIZE_WIDTH)
        roi_mask = make_road_roi_mask(current_frame)

        if METHOD == 'running_avg':
            mask, avg_background = create_running_avg_mask(avg_background, current_gray)
        elif METHOD == 'mog2':
            mask = create_mog2_mask(background_subtractor, current_frame)
        else:
            cap.release()
            raise ValueError(f'METHOD tidak dikenal: {METHOD}')

        mask = apply_roi(mask, roi_mask)
        boxes = postprocess_boxes(find_vehicle_candidates(mask))
        counts_buffer.append(len(boxes))

        if len(counts_buffer) == interval:
            total = sum(counts_buffer)
            summary.append({
                'start_frame': interval_start,
                'end_frame': current_frame_number,
                'total_deteksi': total,
                'rata_rata': total / len(counts_buffer),
                'maks': max(counts_buffer),
                'min': min(counts_buffer),
                'mulai_detik': interval_start / fps,
                'akhir_detik': current_frame_number / fps,
            })
            counts_buffer = []
            interval_start = current_frame_number + 1

        last_read_frame_number = current_frame_number

    cap.release()

    if counts_buffer:
        total = sum(counts_buffer)
        summary.append({
            'start_frame': interval_start,
            'end_frame': last_read_frame_number,
            'total_deteksi': total,
            'rata_rata': total / len(counts_buffer),
            'maks': max(counts_buffer),
            'min': min(counts_buffer),
            'mulai_detik': interval_start / fps,
            'akhir_detik': last_read_frame_number / fps,
        })

    if not summary:
        print('Tidak ada frame yang diproses setelah warmup. Coba kecilkan WARMUP_FRAMES.')
        return []

    print('Review jumlah kendaraan per interval')
    print('Metode          : {}'.format(METHOD))
    print('Interval        : {} frame'.format(interval))
    print('Warmup          : {} frame'.format(min(warmup_frames, last_read_frame_number)))
    print('-' * 88)
    print('{:<18} {:>14} {:>16} {:>8} {:>8} {:>14}'.format(
        'Interval Frame', 'Total Deteksi', 'Rata-rata/Frame', 'Maks', 'Min', 'Mulai Detik'
    ))
    print('-' * 88)

    for row in summary:
        label = '{}-{}'.format(row['start_frame'], row['end_frame'])
        print('{:<18} {:>14} {:>16.2f} {:>8} {:>8} {:>14.2f}'.format(
            label,
            row['total_deteksi'],
            row['rata_rata'],
            row['maks'],
            row['min'],
            row['mulai_detik'],
        ))

    print('-' * 88)
    grand_total = sum(row['total_deteksi'] for row in summary)
    total_frames = sum(row['end_frame'] - row['start_frame'] + 1 for row in summary)
    print('{:<18} {:>14} {:>16.2f}'.format(
        'TOTAL',
        grand_total,
        grand_total / max(total_frames, 1),
    ))

    labels = [f"{row['start_frame']}-{row['end_frame']}" for row in summary]
    totals = [row['total_deteksi'] for row in summary]
    avgs = [row['rata_rata'] for row in summary]

    fig, axes = plt.subplots(1, 2, figsize=(14, 4))

    axes[0].bar(range(len(labels)), totals)
    axes[0].set_title('Total Deteksi per Interval Frame')
    axes[0].set_xlabel('Interval Frame')
    axes[0].set_ylabel('Jumlah Deteksi')
    axes[0].set_xticks(range(len(labels)))
    axes[0].set_xticklabels(labels, rotation=45, ha='right', fontsize=8)

    axes[1].plot(range(len(labels)), avgs, marker='o')
    axes[1].set_title('Rata-rata Kendaraan per Frame')
    axes[1].set_xlabel('Interval Frame')
    axes[1].set_ylabel('Rata-rata')
    axes[1].set_xticks(range(len(labels)))
    axes[1].set_xticklabels(labels, rotation=45, ha='right', fontsize=8)

    plt.tight_layout()
    plt.savefig(save_path, dpi=120, bbox_inches='tight')
    plt.show()
    print('Grafik disimpan ke: {}'.format(save_path))

    return summary


review_summary = review_detections_per_interval(interval=REVIEW_INTERVAL)

